/*
//HTTP:
-HTTP: Protocolo de comunicação web e possui 'verbos'
-Verbos ajudam a identificar ações através da URL. Uma url pode ter mais de um verbo
-Um verbo junto com URL forma uma requisição

//Pricipais verbos:
-GET: Solicita dados
-POST: Cadastra dados
-PAT/PATCH: Atualiza dado
-DELETE: Deleta dado

//Status de uma requisição HTTP:
-Status ajuda a ver como uma requisição foi processada
-Para um requisição, o cliente sempre terá uma resposta
-stats 100 são respostas inforativas ao cliente
-status 200 são quando uma requisição teve sucesso
-status 300 apresentam mensagens de redirecionamento
-status 400 apresentam mensagens de erro
-status 500 representam dados no servidor

 //Resumo
 -Front end envia requisição com um verbo e e url(Ex: GET/users), o servidor web retorna o status e body (Ex: 200 - <página HTML>)
 */
